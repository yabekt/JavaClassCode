package com.class3;

public class ShorthandOperator {
	public static void main(String[]args) {
		int num3=1000;
		num3+=1000;
		System.out.println(num3);
		
		int num2=100;
		num2+=100;
		System.out.println(num2);
		int num4=67;
		num4-=67;
		System.out.println(num4);
		double cakePiece=11;
		cakePiece/=4;
		System.out.println(cakePiece);
		cakePiece=25;
		cakePiece%=7;
		System.out.println(cakePiece);
		
		double d2=123.56;
		int n1=(int)d2;
		System.out.println(n1);
		
		
	}

}
